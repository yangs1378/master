class YOLO_DET:
    def __init__(self, weights='yolov7.pt'):
        self.device = torch.device('cuda:0' if torch.cuda.is_available() else 'cpu')
        self.weights = weights
        self.imgsz = 640
        self.trace = False
        self.augment = False
        self.conf_thres = 0.25
        self.iou_thres = 0.45
        self.stride = 32
        self.classes = None
        self.agnostic_nms = False
        # Initialize
        set_logging()
        self.half = self.device.type != 'cpu'  # half precision only supported on CUDA
        # Load model
        self.model = attempt_load(self.weights, map_location=self.device)  # load FP32 model
        stride = int(self.model.stride.max())  # model stride
        img_sz = check_img_size(self.imgsz, s=stride)  # check img_size

        if self.trace:
            model = TracedModel(self.model, self.device, self.imgsz)

        if self.half:
            self.model.half()  # to FP16
        # Run inference
        if self.device.type != 'cpu':
            self.model(torch.zeros(1, 3, img_sz, img_sz).to(self.device).type_as(next(self.model.parameters())))  # run once

    def img_trans(self, image):
        img = letterbox(image, self.imgsz, stride=self.stride)[0]

        # Convert
        img = img[:, :, ::-1].transpose(2, 0, 1)  # BGR to RGB, to 3x416x416
        img = np.ascontiguousarray(img)
        img = torch.from_numpy(img).to(self.device)
        img = img.half() if self.half else img.float()  # uint8 to fp16/32
        img /= 255.0  # 0 - 255 to 0.0 - 1.0
        if img.ndimension() == 3:
            img = img.unsqueeze(0)
        return img

    def detect(self, image):
        pred = self.model(image, augment=self.augment)[0]
        # Apply NMS
        pred = non_max_suppression(pred, self.conf_thres, self.iou_thres, classes=self.classes, agnostic=self.agnostic_nms)
        return pred

